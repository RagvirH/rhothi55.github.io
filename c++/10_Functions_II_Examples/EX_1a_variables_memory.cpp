#include <iostream>
using namespace std;

int main() {
    cout << endl;

    int i = 5;                  // an integer variable i
    cout << i << "\n";          // the value of i
    cout << &i << "\n";         // the memory address of i (address of operator)
    cout << sizeof(i) << "\n";  // the size of the variable i (based upon its type)

    cout << endl;
    return 0;
}
